    public void atualizarTabela(List<produtos> produto) {
        
        if(produto == null) {
            produto = new ArrayList<>();
        }
        
        String columns[] = {"nome", "Quantidade", "Valor"};
        String dados[][] = new String[produto.size()][columns.length];
        
        int i = 0;
        
        for(produtos p : produto) {
            dados[i] = new String[] {
                String.valueOf(p.getNome()),
                String.valueOf(p.getQuantidade()),
                String.valueOf(p.getValor())
            };
            i++;
        }
        
        DefaultTableModel modelo = new DefaultTableModel(dados, columns);
        tabela.setModel(modelo);
        
    }

