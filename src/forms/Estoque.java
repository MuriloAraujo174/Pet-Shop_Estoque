    private void txtBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyReleased
        String busca = txtBuscar.getText();
        
        ProdutoDAO produtoDao = new ProdutoDAO();
        List<produtos> list = produtoDao.listar3(busca);
        
        if(list.isEmpty()) {
            lblErro.setText("Erro no procura de produtos");
        } else {
            produtos p = list.get(0);
            lblNome.setText(p.getNome());
            lblQuantidade.setText(String.valueOf(p.getQuantidade()));
            lblPreco.setText(String.valueOf(p.getValor()));
            lblErro.setText("");
        }
        
    }//GEN-LAST:event_txtBuscarKeyReleased

